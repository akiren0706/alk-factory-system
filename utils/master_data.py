def fix_indicator_name(ru: str, jp: str) -> str:
    """指標の表示名を正しい日本語に変換する"""
    ru = str(ru).strip() if ru else ""
    jp = str(jp).strip() if jp else ""
    # RUで完全一致する翻訳があれば優先
    if ru in INDICATOR_JP_MAP:
        return INDICATOR_JP_MAP[ru]
    # 前方一致（長い指標名に対応）
    for k, v in INDICATOR_JP_MAP.items():
        if ru.startswith(k):
            return v
    # JP名が不自然なら修正
    if jp in INDICATOR_JP_FIXES:
        return INDICATOR_JP_FIXES[jp]
    # JP名があればそのまま、なければRU名をそのまま返す
    return jp if jp else ru


def translate(text: str) -> str:
    """ロシア語テキストを日本語に変換する（辞書にない場合はそのまま返す）"""
    if not text:
        return text
    t = str(text).strip()
    return FACTORY_MAP.get(t) or AREA_MAP.get(t) or t


def detect_field(header: str, data_type: str) -> str:
    """ロシア語列名からシステムフィールドキーを自動検出する"""
    if not header:
        return "_ignore"
    h = str(header).lower().strip()
    keywords = COLUMN_KEYWORDS_STOPPAGE if data_type == "stoppage" else COLUMN_KEYWORDS_PRODUCTION
    for field, terms in keywords.items():
        if any(t in h for t in terms):
            return field
    return "_ignore"
